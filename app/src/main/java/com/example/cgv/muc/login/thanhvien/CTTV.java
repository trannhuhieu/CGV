package com.example.cgv.muc.login.thanhvien;

public class CTTV {
    int hinhcttv;
    String tencttv;

    public CTTV(int hinhcttv, String tencttv) {
        this.hinhcttv = hinhcttv;
        this.tencttv = tencttv;
    }

    public int getHinhcttv() {
        return hinhcttv;
    }

    public void setHinhcttv(int hinhcttv) {
        this.hinhcttv = hinhcttv;
    }

    public String getTencttv() {
        return tencttv;
    }

    public void setTencttv(String tencttv) {
        this.tencttv = tencttv;
    }
}
