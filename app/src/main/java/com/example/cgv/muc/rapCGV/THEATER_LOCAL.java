package com.example.cgv.muc.rapCGV;

public class THEATER_LOCAL {
    String name, far;

    public THEATER_LOCAL(String name, String far) {
        this.name = name;
        this.far = far;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFar() {
        return far;
    }

    public void setFar(String far) {
        this.far = far;
    }
}
